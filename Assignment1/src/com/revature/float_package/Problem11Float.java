package com.revature.float_package;

public class Problem11Float {
	private float a;
	private float b;
	
	public Problem11Float(float a, float b){
		setA(a);
		setB(b);
	}

	public float getA() {
		return a;
	}

	public void setA(float a) {
		this.a = a;
	}

	public float getB() {
		return b;
	}

	public void setB(float b) {
		this.b = b;
	}
}
