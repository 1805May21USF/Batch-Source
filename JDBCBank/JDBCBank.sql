CREATE OR REPLACE PROCEDURE INSERTACCOUNT
(ACCOUNTID INT, USERNAME VARCHAR2,ACCOUNTTYPE VARCHAR2,ACCOUNTNAME VARCHAR2,BALANCE DECIMAL)
AS 
BEGIN
INSERT INTO ACCOUNT VALUES(GENERATE_ACCOUNTNUM.NEXTVAL,USERNAME,ACCOUNTTYPE,ACCOUNTNAME,BALANCE);
COMMIT;
END;
/

CREATE OR REPLACE PROCEDURE UPDATEACCOUNT
(INACCOUNTID IN INT, INUSERNAME IN VARCHAR2,INACCOUNTTYPE IN VARCHAR2,INACCOUNTNAME IN VARCHAR2,INBALANCE IN DECIMAL)
AS
BEGIN
UPDATE ACCOUNT
SET
--ACCOUNT.ACCOUNTID = INACCOUNT,
ACCOUNT.USERNAME =INUSERNAME,
ACCOUNT.ACCOUNTTYPE=INACCOUNTTYPE,
ACCOUNT.ACCOUNTNAME=INACCOUNTNAME,
ACCOUNT.BALANCE=INBALANCE
WHERE ACCOUNT.ACCOUNTID=INACCOUNTID;
COMMIT;
END;
/

CREATE SEQUENCE GENERATE_ACCOUNTNUM
MINVALUE 1000
MAXVALUE 4000
START WITH 1000
INCREMENT BY 1
CACHE 5;

