package com.revature.q11package;

/**
 * Contains float variables to be accessed
 * from outside of the package.
 * @author Nathaniel Simpson
 *
 */
public class Q11PackageAccess {
	
	private float f1 = 12.34f;
	private float f2 = 42.69f;
	
	public float getF1() {
		return f1;
	}
	public float getF2() {
		return f2;
	}
	
	

}
