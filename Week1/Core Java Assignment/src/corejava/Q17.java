package corejava;

public class Q17 {
	/*Write a program that calculates the simple interest on the principal, 
	 * rate of interest and number of years provided by the user. Enter principal, rate and time 
	 * through the console using the Scanner class.
	Interest = Principal* Rate* Time*/
	public static int Solution(int principal, int rate, int time) {
		return principal * rate * time;
	}
}
